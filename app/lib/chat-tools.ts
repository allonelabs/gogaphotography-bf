// ── save_post ───────────────────────────────────────────────────────────
// Persists a draft post into the business's content library. Reads the
// existing posts via posts-store.readPosts(), appends a new draft, and
// writes back. The /s/content page reads from the same store, so the
// post appears immediately on next render. Closes the lying-bug where
// the chat used to say "Got it. I've saved your post" without any
// backing tool to actually save.

interface SavePostInput {
  slug?: string;
  title?: string;
  body?: string;
  excerpt?: string;
  tags?: string[];
}

async function handleSavePost(
  input: SavePostInput,
): Promise<ToolExecutionResult> {
  const slug = String(input.slug ?? "").trim();
  if (!slug) return { ok: false, summary: "save_post: missing slug" };
  const title = String(input.title ?? "").trim();
  if (!title) return { ok: false, summary: "save_post: missing title" };
  const bodyText = String(input.body ?? "").trim();
  if (!bodyText) return { ok: false, summary: "save_post: missing body" };

  const { readPosts, writePosts, slugify, emptyPost } =
    await import("./posts-store");
  const existing = await readPosts(slug);

  // Ensure post-slug is unique within the spawn.
  const baseSlug =
    slugify(title) || `post-${Math.random().toString(36).slice(2, 8)}`;
  let postSlug = baseSlug;
  let n = 2;
  while (existing.some((p) => p.slug === postSlug)) {
    postSlug = `${baseSlug}-${n++}`;
  }

  const now = new Date().toISOString();
  const cleanBody = bodyText.slice(0, 200_000);
  const cleanExcerpt =
    String(input.excerpt ?? "")
      .trim()
      .slice(0, 400) ||
    cleanBody.split(/\n\n/)[0]?.slice(0, 240) ||
    "";
  const tags = Array.isArray(input.tags)
    ? input.tags
        .map((t) => String(t).toLowerCase().slice(0, 32))
        .filter(Boolean)
        .slice(0, 8)
    : [];

  const empty = emptyPost();
  const newPost = {
    ...empty,
    id: `post_${Math.random().toString(36).slice(2, 10)}`,
    slug: postSlug,
    title: title.slice(0, 200),
    excerpt: cleanExcerpt,
    body: cleanBody,
    tags,
    status: "draft" as const,
    createdAt: now,
    updatedAt: now,
  };

  await writePosts(slug, [newPost, ...existing]);

  return {
    ok: true,
    summary: `Saved "${newPost.title}" as a draft post. Find it under Content → Posts ready to review and publish.`,
    data: {
      postId: newPost.id,
      postSlug: newPost.slug,
      status: newPost.status,
      openAt: `/app/business/${slug}/s/content`,
    },
  };
}

