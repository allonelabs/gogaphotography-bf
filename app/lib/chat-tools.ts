async function handleAddProduct(
  input: AddProductInput,
): Promise<ToolExecutionResult> {
  const slug = String(input.slug ?? "").trim();
  if (!slug) return { ok: false, summary: "add_product: missing slug" };
  if (!input.name || typeof input.name !== "string") {
    return { ok: false, summary: "add_product: missing or invalid name" };
  }
  const priceCents = Math.max(0, Math.round(Number(input.priceCents) || 0));
  if (priceCents <= 0) {
    return {
      ok: false,
      summary:
        "add_product: priceCents must be > 0 (in minor units — $22 = 2200)",
    };
  }

  // Dynamic-import keeps this file lean for non-shop chat scopes.
  const { readProducts, writeProducts } = await import("./shop-store");
  const { rebuildSiteWithProducts } = await import("./site-rebuild");

  const existing = await readProducts(slug);
  const now = new Date().toISOString();
  const productSlug =
    String(input.name)
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .slice(0, 48) || `p${Math.random().toString(36).slice(2, 8)}`;
  // Ensure slug is unique within the spawn.
  let finalSlug = productSlug;
  let n = 2;
  while (existing.some((p) => p.slug === finalSlug)) {
    finalSlug = `${productSlug}-${n++}`;
  }

  const newProduct = {
    id: `p_${Math.random().toString(36).slice(2, 10)}`,
    name: String(input.name).slice(0, 200),
    slug: finalSlug,
    description: String(input.description ?? "").slice(0, 8000),
    priceCents,
    currency: String(input.currency ?? "USD")
      .toUpperCase()
      .slice(0, 3),
    imageName: String(input.imageName ?? "").slice(0, 200),
    inventory: Math.max(0, Math.round(Number(input.inventory) || 1)),
    status: (["draft", "active", "archived"] as const).includes(
      input.status as never,
    )
      ? (input.status as "draft" | "active" | "archived")
      : ("active" as const),
    createdAt: now,
    updatedAt: now,
  };

  await writeProducts(slug, [...existing, newProduct]);

  // Trigger site rebuild so the new product shows up on the public preview.
  const rebuild = await rebuildSiteWithProducts(slug).catch(
    (err) =>
      ({
        ok: false,
        reason: err instanceof Error ? err.message : "rebuild error",
      }) as const,
  );

  return {
    ok: true,
    summary: `Added "${newProduct.name}" to the shop ($${(priceCents / 100).toFixed(2)} ${newProduct.currency}). ${rebuild.ok ? `Site rebuilt — visible at /product/${finalSlug}.` : `Stored, but rebuild ${"reason" in rebuild ? `skipped (${rebuild.reason})` : "pending"}.`}`,
    note: rebuild.ok
      ? undefined
      : "Operator can hit Regenerate · Hero photo or full respawn to force a re-render.",
  };
}

