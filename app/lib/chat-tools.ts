//
// Maps the BUSINESS_TOOLS declared in the chat route to actual server-side
// handlers. The chat route receives a `tool_use` block from the model,
// dispatches here, and feeds the result back to the model in a follow-up
// turn so it can produce a final natural-language reply.
//
// Status (chunk 101):
//   • regenerate_site — RECORDS the intent + extracts brief context.
//     The actual page.tsx emission is queued for chunk 102 (deterministic
//     templates) → chunk 103 (LLM-authored content). This commit makes
//     the round-trip work end-to-end so an operator can repeat the
//     directive's exchange and see real on-disk evidence.
//   • Other tools — return a deferred-stub note. Same idea, queued
//     in priority order: deploy_now (chunk 104) → add_route (chunk 105) →
//     edit_artifact (106) → send_outreach_email (107) → change_pricing
//     (108) → regenerate_brand_identity (109).
//
// Per-tool authorization: the chat route returns the tool result back
// to the model; the model decides whether to mention the deferred-stub
// state in its reply. The client may still surface a "preview"
// confirmation step before this executor runs (chunk 110 UX work).

import "server-only";

import { mkdir, readFile, rename, stat, writeFile } from "node:fs/promises";
import path from "node:path";

import { resolveOutRoot } from "./spawn-loader";
import { callLLM } from "./llm-fallback";

export interface ToolExecutionResult {
  /** Did the tool actually do anything observable. False for deferred stubs. */
  readonly ok: boolean;
  /** Short summary the model can paraphrase to the operator. */
  readonly summary: string;
  /** Structured payload for client-side rendering and the model's follow-up. */
  readonly data?: Record<string, unknown>;
  /** Persistent artifacts written to disk. */
  readonly artifacts?: Array<{ path: string; bytes: number; kind: string }>;
  /** Honest indicator: tmp shadow-root vs real disk. */
  readonly persistence?: "tmp" | "disk";
  /** When ok=false, why (e.g., tool not yet wired). */
  readonly note?: string;
}

const VALID_SPAWN_ID = /^[a-z0-9][a-z0-9.\-_]*$/i;

function isServerless(): boolean {
  return (
    Boolean(process.env["VERCEL"]) || process.env["NODE_ENV"] === "production"
  );
}

function writableRoot(): string {
  return isServerless() ? "/tmp/business-forge-out" : resolveOutRoot();
}

