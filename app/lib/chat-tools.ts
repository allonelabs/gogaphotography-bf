// Translate a restyle patch (picks + hex colors) into one human sentence.
// 2026-05-14 — operator complained that summaries like
//   `Restyled the-comfort-loaf with directive "X". Changed: paperColor=#4CAF50. Live at https://...`
// were too technical.  Now: "Background is now green." etc.
function colorName(hex: string): string {
  const h = hex.toLowerCase();
  if (!/^#[0-9a-f]{6}$/.test(h)) return hex;
  const r = parseInt(h.slice(1, 3), 16);
  const g = parseInt(h.slice(3, 5), 16);
  const b = parseInt(h.slice(5, 7), 16);
  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  if (max - min < 18 && max < 60) return "black";
  if (max - min < 18 && max > 220) return "white";
  if (max - min < 18) return "grey";
  if (r > g && r > b) return r > 200 && g < 120 ? "red" : "warm red";
  if (g > r && g > b) return "green";
  if (b > r && b > g) return r > 120 ? "purple" : "blue";
  if (r > 200 && g > 180 && b < 120) return "yellow";
  if (r > 200 && g > 100 && g < 180 && b < 100) return "orange";
  return hex;
}

const PICK_FRIENDLY_NAME: Record<string, string> = {
  palette: "colour palette",
  fonts: "typography",
  background: "background pattern",
  density: "spacing",
  motion: "motion style",
  cta: "button style",
  radius: "corner radius",
  imageFx: "image treatment",
  typeScale: "type scale",
  navStyle: "navigation",
  footerStyle: "footer",
  hoverEffect: "hover effect",
  dividers: "divider style",
  heroOverlay: "hero overlay",
  wordmarkStyle: "wordmark style",
};

function prettyRestyleSummary(patch: unknown): string {
  if (!patch || typeof patch !== "object") return "Done.";
  const p = patch as Record<string, unknown>;
  const bits: string[] = [];
  const params = (p["params"] as Record<string, string> | undefined) ?? {};
  if (params.paperColor)
    bits.push(`Background is now ${colorName(params.paperColor)}.`);
  if (params.accentColor)
    bits.push(`Accent is now ${colorName(params.accentColor)}.`);
  if (params.inkColor)
    bits.push(`Text colour is now ${colorName(params.inkColor)}.`);
  const picks = (p["picks"] as Record<string, string> | undefined) ?? {};
  for (const [k, v] of Object.entries(picks)) {
    const name = PICK_FRIENDLY_NAME[k] ?? k;
    bits.push(
      `${name.charAt(0).toUpperCase()}${name.slice(1)}: ${v.replace(/-/g, " ")}.`,
    );
  }
  return bits.length > 0 ? bits.join(" ") : "Done.";
}

