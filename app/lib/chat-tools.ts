// ── add_contact ─────────────────────────────────────────────────────────
// Persists a manual CRM contact to the business's contacts.json. Uses
// the existing readManualContacts / writeManualContacts pair from
// contacts-store; identity is the lowercased email address (which is
// also how form-submission + order activity is later joined).

interface AddContactInput {
  slug?: string;
  email?: string;
  name?: string;
  phone?: string;
  tags?: string[];
  notes?: string;
}

async function handleAddContact(
  input: AddContactInput,
): Promise<ToolExecutionResult> {
  const slug = String(input.slug ?? "").trim();
  if (!slug) return { ok: false, summary: "add_contact: missing slug" };
  const email = String(input.email ?? "")
    .trim()
    .toLowerCase();
  if (!email || !email.includes("@")) {
    return {
      ok: false,
      summary: "add_contact: email is required (and must look like an email)",
    };
  }

  const { readManualContacts, writeManualContacts } =
    await import("./contacts-store");
  const existing = await readManualContacts(slug);

  // De-dupe by email — if the operator adds the same person twice we
  // merge fields instead of writing two rows. The store keys activity
  // by email anyway, so the second add would be invisible otherwise.
  const idx = existing.findIndex((c) => c.email.toLowerCase() === email);
  const now = new Date().toISOString();
  const tags = Array.isArray(input.tags)
    ? input.tags
        .map((t) => String(t).toLowerCase().slice(0, 32))
        .filter(Boolean)
        .slice(0, 8)
    : [];
  const name =
    typeof input.name === "string" ? input.name.trim().slice(0, 200) : "";
  const phone =
    typeof input.phone === "string" ? input.phone.trim().slice(0, 64) : "";
  const notes =
    typeof input.notes === "string" ? input.notes.trim().slice(0, 4000) : "";

  if (idx >= 0) {
    const prev = existing[idx]!;
    const merged = {
      email,
      name: name || prev.name,
      phone: phone || prev.phone,
      tags: Array.from(new Set([...(prev.tags ?? []), ...tags])).slice(0, 8),
      notes: notes
        ? prev.notes
          ? `${prev.notes}\n\n${notes}`
          : notes
        : prev.notes,
      addedAt: prev.addedAt ?? now,
    };
    const next = [...existing];
    next[idx] = merged;
    await writeManualContacts(slug, next);
    return {
      ok: true,
      summary: `Updated existing contact ${email}. Find them under Customers.`,
      data: {
        email,
        merged: true,
        openAt: `/app/business/${slug}/s/customers`,
      },
    };
  }

  const newContact = { email, name, phone, tags, notes, addedAt: now };
  await writeManualContacts(slug, [...existing, newContact]);
  return {
    ok: true,
    summary: `Added ${name || email} to your contacts. Find them under Customers.`,
    data: { email, merged: false, openAt: `/app/business/${slug}/s/customers` },
  };
}

