async function handleChangePricing(
  input: ChangePricingInput,
): Promise<ToolExecutionResult> {
  const slug = typeof input.slug === "string" ? input.slug : "";
  if (!VALID_SPAWN_ID.test(slug)) {
    return { ok: false, summary: `Invalid spawn id "${slug}"` };
  }
  const tiersRaw = Array.isArray(input.tiers) ? input.tiers : [];
  if (tiersRaw.length === 0) {
    return {
      ok: false,
      summary: "tiers[] is empty — provide at least one pricing tier.",
    };
  }
  if (tiersRaw.length > 6) {
    return {
      ok: false,
      summary: `tiers[] has ${tiersRaw.length} entries; cap is 6.`,
    };
  }

  // Validate + normalize each tier.
  const tiers: PricingTier[] = [];
  for (const t of tiersRaw) {
    const name = typeof t.name === "string" ? t.name.trim() : "";
    const priceUsdMonthly =
      typeof t.priceUsdMonthly === "number" &&
      Number.isFinite(t.priceUsdMonthly) &&
      t.priceUsdMonthly >= 0
        ? Math.round(t.priceUsdMonthly)
        : -1;
    if (!name || priceUsdMonthly < 0) {
      return {
        ok: false,
        summary: `Invalid tier: each tier needs a non-empty name and a non-negative priceUsdMonthly. Got ${JSON.stringify(t)}.`,
      };
    }
    const features = Array.isArray(t.features)
      ? t.features
          .filter((f): f is string => typeof f === "string" && f.length > 0)
          .slice(0, 12)
      : [];
    tiers.push({ name, priceUsdMonthly, features });
  }

  const root = writableRoot();
  const safeStamp = new Date().toISOString().replace(/[:.]/g, "-");
  const siteAppDir = path.join(root, slug, "site", "app");
  const targetPath = routeToPagePath(siteAppDir, "/pricing");

  let backupRel: string | undefined;
  try {
    await stat(targetPath);
    const backupPath = `${targetPath}.${safeStamp}.bak`;
    await rename(targetPath, backupPath);
    backupRel = path.relative(root, backupPath);
  } catch {
    /* no prior pricing page */
  }

  const brief = await readBriefIfPresent(slug);
  const tsx = customPricingTemplate(brief, slug, tiers);

  try {
    await mkdir(path.dirname(targetPath), { recursive: true });
    await writeFile(targetPath, tsx, "utf8");
  } catch (e) {
    return {
      ok: false,
      summary: `Failed to write /pricing: ${e instanceof Error ? e.message : String(e)}`,
    };
  }

  // Audit + tier snapshot for replay.
  const intentDir = path.join(root, slug, ".chat-tools");
  const intentPath = path.join(intentDir, `change_pricing-${safeStamp}.json`);
  const audit = {
    tool: "change_pricing",
    slug,
    tiers,
    requestedAt: new Date().toISOString(),
    page: path.relative(root, targetPath),
    backup: backupRel,
  };
  await mkdir(intentDir, { recursive: true });
  await writeFile(intentPath, JSON.stringify(audit, null, 2), "utf8");

  const persistence: "tmp" | "disk" = isServerless() ? "tmp" : "disk";
  const tierSummary = tiers
    .map((t) => `${t.name} $${t.priceUsdMonthly}`)
    .join(" · ");
  return {
    ok: true,
    summary: `Updated /pricing on ${slug} with ${tiers.length} tier${tiers.length === 1 ? "" : "s"}: ${tierSummary}. ${backupRel ? "Previous version backed up." : "No prior pricing page existed."} Operator should call deploy_now to push live.`,
    data: { slug, tiers, backup: backupRel },
    artifacts: [
      {
        path: path.relative(root, targetPath),
        bytes: Buffer.byteLength(tsx, "utf8"),
        kind: "page",
      },
      {
        path: path.relative(root, intentPath),
        bytes: Buffer.byteLength(JSON.stringify(audit), "utf8"),
        kind: "tool-intent",
      },
    ],
    persistence,
  };
}

