function templateForRoute(
  route: string,
  brief: BriefShape | null,
  slug: string,
): string {
  switch (route) {
    case "/":
      return homeTemplate(brief, slug);
    case "/about":
      return aboutTemplate(brief, slug);
    case "/pricing":
      return pricingTemplate(brief, slug);
    case "/contact":
      return contactTemplate(brief, slug);
    default:
      return genericTemplate(route, brief, slug);
  }
}

// Map a route ('/about/team') to its app-router file path
// (`app/about/team/page.tsx`). Strips leading slash; '/' → 'page.tsx'.
function routeToPagePath(siteAppDir: string, route: string): string {
  const parts = route
    .replace(/^\/+|\/+$/g, "")
    .split("/")
    .filter(Boolean);
  return path.join(siteAppDir, ...parts, "page.tsx");
}

async function readBriefIfPresent(
  slug: string,
): Promise<{ name?: string; tagline?: string; vertical?: string } | null> {
  const candidates = [
    path.join(resolveOutRoot(), slug, "brand", "name-tagline.json"),
    path.join(
      resolveOutRoot(),
      slug,
      ".cells",
      "brand-forge.name-tagline.json",
    ),
  ];
  for (const c of candidates) {
    try {
      const raw = await readFile(c, "utf8");
      const parsed = JSON.parse(raw) as {
        name?: string;
        tagline?: string;
        output?: { name?: string; tagline?: string };
        vertical?: string;
      };
      const name = parsed.name ?? parsed.output?.name;
      const tagline = parsed.tagline ?? parsed.output?.tagline;
      if (name || tagline) return { name, tagline, vertical: parsed.vertical };
    } catch {
      /* try next */
    }
  }
  return null;
}

async function handleRegenerateSite(
  input: RegenerateSiteInput,
): Promise<ToolExecutionResult> {
  const slug = typeof input.slug === "string" ? input.slug : "";
  if (!VALID_SPAWN_ID.test(slug)) {
    return {
      ok: false,
      summary: `Invalid spawn id "${slug}"`,
      note: "Bad input — model picked the wrong slug.",
    };
  }
  const directive =
    typeof input.directive === "string" ? input.directive.slice(0, 400) : "";

  // CANONICAL re-render path: rebuildSiteWithProducts reads the spawn's
  // stored site-variant.json + current products/categories + runs the
  // FULL renderSiteByTemplate pipeline (vertical-aware, all polish chunks
  // 131-156 land here). Earlier versions of this handler wrote hand-rolled
  // placeholder TSX to disk that bypassed the entire template renderer —
  // operators asking chat to "rebuild my site" got a downgrade from their
  // original spawn. Fixed in chunk 157.
  //
  // `routes` and `directive` are recorded in the intent log but not yet
  // applied: the canonical path re-renders ALL routes with the latest
  // template code. Per-route + per-directive deltas land in a follow-up
  // (would need an LLM call to remix content per directive).

  const root = writableRoot();
  const safeStamp = new Date().toISOString().replace(/[:.]/g, "-");
  const intentDir = path.join(root, slug, ".chat-tools");
  const intentPath = path.join(intentDir, `regenerate_site-${safeStamp}.json`);

  const brief = await readBriefIfPresent(slug);

  // Dynamic import to avoid pulling the heavy renderer into modules that
  // only consume the simpler chat-tool result shape.
  const { rebuildSiteWithProducts } = await import("./site-rebuild");
  const result = await rebuildSiteWithProducts(slug);

  const intent = {
    tool: "regenerate_site",
    slug,
    directive: directive || undefined,
    requestedAt: new Date().toISOString(),
    brief,
    canonicalRebuild: result,
  };
  await mkdir(intentDir, { recursive: true });
  await writeFile(intentPath, JSON.stringify(intent, null, 2), "utf8");

  const persistence: "tmp" | "disk" = isServerless() ? "tmp" : "disk";
  const briefBit = brief?.name ? ` ("${brief.name}")` : "";
  const ok = result.ok;
  const written: Array<{
    path: string;
    bytes: number;
    kind: string;
    backup?: string;
  }> = [];
  const failed: Array<{ route: string; reason: string }> = ok
    ? []
    : [{ route: "site", reason: result.reason ?? "rebuild failed" }];
  written.push({
    path: path.relative(root, intentPath),
    bytes: Buffer.byteLength(JSON.stringify(intent), "utf8"),
    kind: "tool-intent",
  });
  if (ok && result.routes) {
    for (const [route, url] of Object.entries(result.routes)) {
      written.push({ path: url, bytes: 0, kind: `page:${route}` });
    }
  }
  const okCount = ok ? Object.keys(result.routes ?? {}).length : 0;
  const failCount = ok ? 0 : 1;
  const directiveNote = directive
    ? ` (directive "${directive.slice(0, 60)}" noted but not yet applied per-route — full template re-render produced).`
    : "";
  const summary = ok
    ? `Re-rendered ${okCount} route${okCount === 1 ? "" : "s"} for ${slug}${briefBit} via the canonical pipeline.${directiveNote} Pages live at /api/spawn-preview/${slug}/<route>.`
    : `Site rebuild failed for ${slug}${briefBit}: ${result.reason ?? "unknown"}. ${result.reason === "no site-variant artifact" ? "This spawn predates the variant pipeline — re-spawn to upgrade." : ""}`;

  return {
    ok: failCount === 0 && okCount > 0,
    summary,
    data: {
      slug,
      directive: directive || undefined,
      brief: brief ?? undefined,
      written,
      failed,
      canonicalRebuild: result,
    },
    artifacts: written,
    persistence,
    note: undefined,
  };
}

// ── add_route handler ──────────────────────────────────────────────────
//
// Small variant of regenerate_site for "add a single page" requests
// like "add an FAQ" or "make a /press page". Doesn't touch existing
// pages — only writes a new page.tsx if the route doesn't already
// exist. The `intent` field shapes the page tone (informational vs
// landing vs call-to-action) — currently surfaces in the description
// metadata; richer per-intent templating can land later.

interface AddRouteInput {
  slug?: string;
  route?: string;
  intent?: string;
}

async function handleAddRoute(
  input: AddRouteInput,
): Promise<ToolExecutionResult> {
  const slug = typeof input.slug === "string" ? input.slug : "";
  if (!VALID_SPAWN_ID.test(slug)) {
    return {
      ok: false,
      summary: `Invalid spawn id "${slug}"`,
      note: "Bad input — model picked the wrong slug.",
    };
  }
  const route = typeof input.route === "string" ? input.route : "";
  if (!route || !route.startsWith("/")) {
    return {
      ok: false,
      summary: `Invalid route "${route}" — must start with "/"`,
      note: "Model emitted a bad route shape.",
    };
  }
  const intent =
    typeof input.intent === "string" ? input.intent.slice(0, 400) : "";

  const root = writableRoot();
  const safeStamp = new Date().toISOString().replace(/[:.]/g, "-");
  const siteAppDir = path.join(root, slug, "site", "app");
  const targetPath = routeToPagePath(siteAppDir, route);

  // Don't clobber existing routes — that's regenerate_site's job, not ours.
  try {
    await stat(targetPath);
    return {
      ok: false,
      summary: `Route ${route} already exists for ${slug}. Use regenerate_site if you want to overwrite, or pick a different route.`,
      note: "add_route refuses to clobber. The model should retry with a new route or escalate to the operator.",
    };
  } catch {
    // Route is free — proceed.
  }

  const brief = await readBriefIfPresent(slug);
  const tsx = templateForRoute(route, brief, slug);

  try {
    await mkdir(path.dirname(targetPath), { recursive: true });
    await writeFile(targetPath, tsx, "utf8");
  } catch (e) {
    return {
      ok: false,
      summary: `Failed to write ${route}: ${e instanceof Error ? e.message : String(e)}`,
      note: "Filesystem write failed.",
    };
  }

  // Audit alongside in .chat-tools/.
  const intentDir = path.join(root, slug, ".chat-tools");
  const intentPath = path.join(intentDir, `add_route-${safeStamp}.json`);
  const auditDoc = {
    tool: "add_route",
    slug,
    route,
    intent: intent || undefined,
    requestedAt: new Date().toISOString(),
    brief,
    page: path.relative(root, targetPath),
  };
  await mkdir(intentDir, { recursive: true });
  await writeFile(intentPath, JSON.stringify(auditDoc, null, 2), "utf8");

  const persistence: "tmp" | "disk" = isServerless() ? "tmp" : "disk";
  const briefBit = brief?.name ? ` ("${brief.name}")` : "";
  return {
    ok: true,
    summary: `Added new route ${route} to ${slug}${briefBit}. Page rendered with ${routeTitle(route).toLowerCase()}-flavored layout.`,
    data: {
      slug,
      route,
      intent: intent || undefined,
      brief: brief ?? undefined,
    },
    artifacts: [
      {
        path: path.relative(root, targetPath),
        bytes: Buffer.byteLength(tsx, "utf8"),
        kind: "page",
      },
      {
        path: path.relative(root, intentPath),
        bytes: Buffer.byteLength(JSON.stringify(auditDoc), "utf8"),
        kind: "tool-intent",
      },
    ],
    persistence,
    note:
      persistence === "tmp"
        ? "Page written to /tmp; survives until lambda restarts."
        : undefined,
  };
}

// ── deploy_now handler ─────────────────────────────────────────────────
//
// Triggers a fresh production deploy so changes from regenerate_site /
// add_route / edit_artifact become visible. We POST to a Vercel Deploy
// Hook URL if one is configured for this spawn (preferred) or globally
// (fallback). If neither is configured, the handler is honest about
// being unable to actually deploy and records the request anyway, so
// the operator knows the limit + can wire the hook later.
//
// Per-spawn hook env var convention: `VERCEL_DEPLOY_HOOK_<SLUG_UPPER_NO_DASH>`.
// Global fallback: `VERCEL_DEPLOY_HOOK_DEFAULT`.

interface DeployNowInput {
  slug?: string;
}

async function handleDeployNow(
  input: DeployNowInput,
): Promise<ToolExecutionResult> {
  const slug = typeof input.slug === "string" ? input.slug : "";
  if (!VALID_SPAWN_ID.test(slug)) {
    return { ok: false, summary: `Invalid spawn id "${slug}"` };
  }

  const root = writableRoot();
  const safeStamp = new Date().toISOString().replace(/[:.]/g, "-");
  const intentDir = path.join(root, slug, ".chat-tools");
  const intentPath = path.join(intentDir, `deploy_now-${safeStamp}.json`);

  // Prefer per-spawn hook → fall back to global default. Slug is
  // normalized to uppercase + non-alphanumerics → underscore so the
  // env-var name is shell-safe.
  const slugKey = slug.toUpperCase().replace(/[^A-Z0-9]+/g, "_");
  const perSpawnHook = process.env[`VERCEL_DEPLOY_HOOK_${slugKey}`];
  const globalHook = process.env["VERCEL_DEPLOY_HOOK_DEFAULT"];
  const hookUrl = perSpawnHook ?? globalHook;
  const hookSource: "per-spawn" | "global" | "none" = perSpawnHook
    ? "per-spawn"
    : globalHook
      ? "global"
      : "none";

  const requestedAt = new Date().toISOString();

  if (!hookUrl) {
    const audit = {
      tool: "deploy_now",
      slug,
      requestedAt,
      hookSource: "none",
      triggered: false,
    };
    await mkdir(intentDir, { recursive: true });
    await writeFile(intentPath, JSON.stringify(audit, null, 2), "utf8");
    return {
      ok: false,
      summary: `Can't trigger a deploy for ${slug} yet — no Vercel deploy hook configured. Operator needs to set VERCEL_DEPLOY_HOOK_${slugKey} or VERCEL_DEPLOY_HOOK_DEFAULT in the env.`,
      data: { slug, hookSource, triggered: false },
      artifacts: [
        {
          path: path.relative(root, intentPath),
          bytes: Buffer.byteLength(JSON.stringify(audit), "utf8"),
          kind: "tool-intent",
        },
      ],
      persistence: isServerless() ? "tmp" : "disk",
      note: "Intent recorded. Real deploy needs the hook URL paste.",
    };
  }

  // Vercel deploy hooks accept POST with no body. They return JSON
  // with `job.id` and `job.state` (PENDING). We don't poll completion;
  // operator can watch progress on the Vercel dashboard.
  let triggered = false;
  let hookResponse: {
    jobId?: string;
    state?: string;
    raw?: string;
    error?: string;
  } = {};
  try {
    const res = await fetch(hookUrl, { method: "POST" });
    const text = await res.text();
    if (res.ok) {
      triggered = true;
      try {
        const parsed = JSON.parse(text) as {
          job?: { id?: string; state?: string };
        };
        hookResponse = {
          jobId: parsed.job?.id,
          state: parsed.job?.state,
          raw: text.slice(0, 200),
        };
      } catch {
        hookResponse = { raw: text.slice(0, 200) };
      }
    } else {
      hookResponse = { error: `${res.status}: ${text.slice(0, 200)}` };
    }
  } catch (e) {
    hookResponse = { error: e instanceof Error ? e.message : String(e) };
  }

  const audit = {
    tool: "deploy_now",
    slug,
    requestedAt,
    hookSource,
    triggered,
    hookResponse,
  };
  await mkdir(intentDir, { recursive: true });
  await writeFile(intentPath, JSON.stringify(audit, null, 2), "utf8");

  return {
    ok: triggered,
    summary: triggered
      ? `Triggered Vercel deploy for ${slug} via ${hookSource} hook${hookResponse.jobId ? ` (job ${hookResponse.jobId})` : ""}. Build in progress on the Vercel dashboard; usually completes in 1-2 minutes.`
      : `Deploy hook responded with an error: ${hookResponse.error ?? "unknown"}.`,
    data: { slug, hookSource, triggered, hookResponse },
    artifacts: [
      {
        path: path.relative(root, intentPath),
        bytes: Buffer.byteLength(JSON.stringify(audit), "utf8"),
        kind: "tool-intent",
      },
    ],
    persistence: isServerless() ? "tmp" : "disk",
    note: triggered
      ? "The next page-load on the spawned site URL will see the new content once Vercel finishes the build."
      : undefined,
  };
}

// ── edit_artifact handler ──────────────────────────────────────────────
//
// Replaces the contents of a single file under out/<slug>/site/. Path
// safety is strict: resolved path must stay inside that directory (no
// `..` escape, no absolute paths). Existing file backed up to
// <path>.<ts>.bak before overwrite. New file size capped at 256KB —
// chat agents shouldn't be writing megabytes of single-file content.
//
// Use cases: "tighten the homepage hero copy", "fix that typo on
// /about", "make the footer say 2026 instead of 2025". Coarser-grained
// changes (multi-page) should go through regenerate_site.

interface EditArtifactInput {
  slug?: string;
  artifactPath?: string;
  newContent?: string;
  reason?: string;
}

const MAX_ARTIFACT_BYTES = 256 * 1024;

async function handleEditArtifact(
  input: EditArtifactInput,
): Promise<ToolExecutionResult> {
  const slug = typeof input.slug === "string" ? input.slug : "";
  if (!VALID_SPAWN_ID.test(slug)) {
    return { ok: false, summary: `Invalid spawn id "${slug}"` };
  }
  const artifactPath =
    typeof input.artifactPath === "string" ? input.artifactPath : "";
  if (
    !artifactPath ||
    artifactPath.startsWith("/") ||
    artifactPath.includes("\0")
  ) {
    return {
      ok: false,
      summary: `artifactPath must be a relative path under out/<slug>/, got "${artifactPath}"`,
    };
  }
  const newContent =
    typeof input.newContent === "string" ? input.newContent : "";
  if (!newContent) {
    return {
      ok: false,
      summary:
        "newContent is empty — refusing to write a 0-byte file. Use regenerate_site if you want to delete then rebuild.",
    };
  }
  const bytes = Buffer.byteLength(newContent, "utf8");
  if (bytes > MAX_ARTIFACT_BYTES) {
    return {
      ok: false,
      summary: `newContent is ${bytes} bytes; cap is ${MAX_ARTIFACT_BYTES}. Split into multiple edit_artifact calls or use regenerate_site.`,
    };
  }
  const reason =
    typeof input.reason === "string" ? input.reason.slice(0, 400) : "";

  const root = writableRoot();
  const spawnRoot = path.join(root, slug);
  // Resolve target inside spawn root and verify no escape. We allow
  // anywhere under out/<slug>/ (not just /site/) so the model can edit
  // .cells/, brand/, or the manifest if it really needs to — ChatTools
  // boundary already requires the model to know what it's doing.
  const target = path.resolve(spawnRoot, artifactPath);
  const spawnRootResolved = path.resolve(spawnRoot);
  if (
    target !== spawnRootResolved &&
    !target.startsWith(spawnRootResolved + path.sep)
  ) {
    return {
      ok: false,
      summary: `Path "${artifactPath}" resolves outside the spawn dir. Refusing.`,
    };
  }

  const safeStamp = new Date().toISOString().replace(/[:.]/g, "-");
  let backupRel: string | undefined;
  try {
    await stat(target);
    const backupPath = `${target}.${safeStamp}.bak`;
    await rename(target, backupPath);
    backupRel = path.relative(root, backupPath);
  } catch {
    // No prior version — nothing to back up.
  }

  try {
    await mkdir(path.dirname(target), { recursive: true });
    await writeFile(target, newContent, "utf8");
  } catch (e) {
    return {
      ok: false,
      summary: `Failed to write ${artifactPath}: ${e instanceof Error ? e.message : String(e)}`,
    };
  }

  // Audit log alongside.
  const intentDir = path.join(root, slug, ".chat-tools");
  const intentPath = path.join(intentDir, `edit_artifact-${safeStamp}.json`);
  const audit = {
    tool: "edit_artifact",
    slug,
    artifactPath,
    reason: reason || undefined,
    requestedAt: new Date().toISOString(),
    bytes,
    backup: backupRel,
  };
  await mkdir(intentDir, { recursive: true });
  await writeFile(intentPath, JSON.stringify(audit, null, 2), "utf8");

  const persistence: "tmp" | "disk" = isServerless() ? "tmp" : "disk";
  return {
    ok: true,
    summary: `Edited ${artifactPath} (${bytes}B). ${backupRel ? `Previous version saved at ${backupRel.split("/").pop()}.` : "No prior version existed."}${reason ? ` Reason: ${reason}` : ""}`,
    data: {
      slug,
      artifactPath,
      bytes,
      reason: reason || undefined,
      backup: backupRel,
    },
    artifacts: [
      { path: path.relative(root, target), bytes, kind: "edit" },
      {
        path: path.relative(root, intentPath),
        bytes: Buffer.byteLength(JSON.stringify(audit), "utf8"),
        kind: "tool-intent",
      },
    ],
    persistence,
  };
}

// ── send_outreach_email handler ────────────────────────────────────────
//
// Appends a row to out/<slug>/site/data/queued-emails.jsonl matching the
// shape the existing dispatchQueuedEmails handler uses. Status starts
// 'queued' — the operator drains via the existing UI button or the auto
// drainer once Resend is wired.
//
// We don't attempt the actual Resend POST here — the dispatcher already
// handles per-spawn Resend config + dry-run mode + bounce tracking. This
// tool's job is just to create the queue row from a chat conversation.

interface SendOutreachEmailInput {
  slug?: string;
  to?: string;
  subject?: string;
  body?: string;
  replyTo?: string;
}

async function appendJsonl(
  filePath: string,
  row: Record<string, unknown>,
): Promise<void> {
  await mkdir(path.dirname(filePath), { recursive: true });
  const line = JSON.stringify(row) + "\n";
  // Append-only — read previous content (if any) and rewrite. Real
  // workloads should use a streaming append, but spawn ledgers stay
  // small enough that this is fine.
  let prior = "";
  try {
    prior = await readFile(filePath, "utf8");
  } catch {
    /* file doesn't exist yet */
  }
  await writeFile(filePath, prior + line, "utf8");
}

async function handleSendOutreachEmail(
  input: SendOutreachEmailInput,
): Promise<ToolExecutionResult> {
  const slug = typeof input.slug === "string" ? input.slug : "";
  if (!VALID_SPAWN_ID.test(slug)) {
    return { ok: false, summary: `Invalid spawn id "${slug}"` };
  }
  const to = typeof input.to === "string" ? input.to.trim() : "";
  if (!to.includes("@") || to.length > 320) {
    return {
      ok: false,
      summary: `Invalid recipient "${to}". Need a real email address.`,
    };
  }
  const subject =
    typeof input.subject === "string" ? input.subject.trim().slice(0, 200) : "";
  const body = typeof input.body === "string" ? input.body.trim() : "";
  if (!subject || !body) {
    return { ok: false, summary: "Subject and body are both required." };
  }
  if (body.length > 32_000) {
    return {
      ok: false,
      summary: `Body is ${body.length} chars; cap is 32000. Trim before queueing.`,
    };
  }
  const replyTo = typeof input.replyTo === "string" ? input.replyTo.trim() : "";

  const root = writableRoot();
  const safeStamp = new Date().toISOString().replace(/[:.]/g, "-");
  const queuePath = path.join(
    root,
    slug,
    "site",
    "data",
    "queued-emails.jsonl",
  );
  const queueRow = {
    queuedAt: new Date().toISOString(),
    template: "custom-outreach",
    kind: "custom-outreach",
    to,
    subject,
    body,
    replyTo: replyTo || undefined,
    status: "queued",
    queuedBy: "chat-tool",
  };
  await appendJsonl(queuePath, queueRow);

  // Audit row alongside .chat-tools/.
  const intentDir = path.join(root, slug, ".chat-tools");
  const intentPath = path.join(
    intentDir,
    `send_outreach_email-${safeStamp}.json`,
  );
  const audit = {
    tool: "send_outreach_email",
    slug,
    to,
    subject,
    bodyLength: body.length,
    replyTo: replyTo || undefined,
    queuePath: path.relative(root, queuePath),
    requestedAt: new Date().toISOString(),
  };
  await mkdir(intentDir, { recursive: true });
  await writeFile(intentPath, JSON.stringify(audit, null, 2), "utf8");

  const persistence: "tmp" | "disk" = isServerless() ? "tmp" : "disk";
  return {
    ok: true,
    summary: `Queued outreach to ${to} ("${subject}", ${body.length} chars). Drains on the next dispatchQueuedEmails tick or via the operator's "Drain now" button on /s/mails. Custom-outreach template — body sent verbatim.`,
    data: { slug, to, subject, bodyLength: body.length, status: "queued" },
    artifacts: [
      {
        path: path.relative(root, queuePath),
        bytes: Buffer.byteLength(JSON.stringify(queueRow), "utf8"),
        kind: "queue-row",
      },
      {
        path: path.relative(root, intentPath),
        bytes: Buffer.byteLength(JSON.stringify(audit), "utf8"),
        kind: "tool-intent",
      },
    ],
    persistence,
    note:
      persistence === "tmp"
        ? "Queue row in /tmp; survives until lambda restarts. Operator should drain promptly."
        : undefined,
  };
}

