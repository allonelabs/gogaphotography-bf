interface BriefShape {
  name?: string;
  tagline?: string;
  vertical?: string;
}

function escapeTsx(s: string): string {
  // Double-curly braces not needed in JSX text content; just escape the
  // backtick (none of our briefs contain them but be safe) and the
  // closing template-literal sequence ${ which JS would interpolate.
  return s.replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

function safeName(brief: BriefShape | null, slug: string): string {
  return escapeTsx((brief?.name ?? slug).trim() || slug);
}

function safeTagline(brief: BriefShape | null): string {
  const t = brief?.tagline?.trim();
  return escapeTsx(t || "Built with AllOnce. One prompt, one running company.");
}

function metadataBlock(name: string, route: string, tagline: string): string {
  const title = route === "/" ? name : `${routeTitle(route)} · ${name}`;
  // Keep description short; tagline can be hundreds of chars.
  const description =
    tagline.length > 160 ? tagline.slice(0, 157) + "…" : tagline;
  return `export const metadata = {
  title: ${JSON.stringify(title)},
  description: ${JSON.stringify(description)},
};`;
}

function routeTitle(route: string): string {
  if (route === "/") return "Home";
  return route
    .replace(/^\//, "")
    .split("/")[0]!
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

