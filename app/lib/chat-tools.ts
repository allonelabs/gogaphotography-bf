// ── add_category ──────────────────────────────────────────────────────

async function handleAddCategory(input: {
  slug?: string;
  name?: string;
  description?: string;
}): Promise<ToolExecutionResult> {
  const slug = String(input.slug ?? "").trim();
  if (!slug) return { ok: false, summary: "add_category: missing slug" };
  if (!input.name || typeof input.name !== "string") {
    return { ok: false, summary: "add_category: missing or invalid name" };
  }
  const { readCategories, writeCategories } = await import("./shop-store");
  const { rebuildSiteWithProducts } = await import("./site-rebuild");
  const existing = await readCategories(slug);
  const now = new Date().toISOString();
  const baseSlug =
    String(input.name)
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .slice(0, 48) || `c${Math.random().toString(36).slice(2, 8)}`;
  let finalSlug = baseSlug;
  let n = 2;
  while (existing.some((c) => c.slug === finalSlug)) {
    finalSlug = `${baseSlug}-${n++}`;
  }
  const newCategory = {
    id: `c_${Math.random().toString(36).slice(2, 10)}`,
    name: String(input.name).slice(0, 200),
    slug: finalSlug,
    parentId: null,
    sortOrder: existing.length,
    description: String(input.description ?? "").slice(0, 4000),
    createdAt: now,
    updatedAt: now,
  };
  await writeCategories(slug, [...existing, newCategory]);
  const rebuild = await rebuildSiteWithProducts(slug).catch(
    (err) =>
      ({
        ok: false,
        reason: err instanceof Error ? err.message : "rebuild error",
      }) as const,
  );
  return {
    ok: true,
    summary: `Added category "${newCategory.name}" — live at /category/${finalSlug}. ${rebuild.ok ? "Site rebuilt." : `Stored, rebuild ${"reason" in rebuild ? `skipped (${rebuild.reason})` : "pending"}.`}`,
  };
}

// ── update_order_status ────────────────────────────────────────────────

async function handleUpdateOrderStatus(input: {
  slug?: string;
  orderId?: string;
  status?: string;
}): Promise<ToolExecutionResult> {
  const slug = String(input.slug ?? "").trim();
  const orderId = String(input.orderId ?? "").trim();
  const status = String(input.status ?? "").trim() as
    | "pending"
    | "paid"
    | "fulfilled"
    | "refunded";
  if (!slug || !orderId)
    return {
      ok: false,
      summary: "update_order_status: missing slug or orderId",
    };
  if (!["pending", "paid", "fulfilled", "refunded"].includes(status)) {
    return {
      ok: false,
      summary: `update_order_status: invalid status "${status}" — must be pending/paid/fulfilled/refunded`,
    };
  }
  const { updateOrderStatus } = await import("./shop-store");
  const updated = await updateOrderStatus(slug, orderId, status);
  if (!updated)
    return {
      ok: false,
      summary: `update_order_status: order ${orderId} not found`,
    };
  return {
    ok: true,
    summary: `Order ${orderId} → ${status}. Customer: ${updated.customerName} <${updated.customerEmail}>, $${(updated.totalCents / 100).toFixed(2)}.`,
  };
}

// ── set_discount ───────────────────────────────────────────────────────

interface SetDiscountInput {
  slug: string;
  code: string;
  kind: "percentage" | "fixed" | "free-shipping";
  amount?: number;
  minOrderCents?: number;
  maxUses?: number;
  active?: boolean;
}

async function handleSetDiscount(
  input: SetDiscountInput,
): Promise<ToolExecutionResult> {
  const slug = String(input.slug ?? "").trim();
  if (!slug) return { ok: false, summary: "set_discount: missing slug" };
  const code = String(input.code ?? "")
    .trim()
    .toUpperCase()
    .replace(/[^A-Z0-9-]/g, "")
    .slice(0, 32);
  if (!code)
    return { ok: false, summary: "set_discount: invalid or empty code" };
  // Discount schema supports percentage + fixed; we map free-shipping → fixed 0 with maxUses cap (no built-in shipping type yet).
  if (!["percentage", "fixed", "free-shipping"].includes(input.kind)) {
    return {
      ok: false,
      summary: `set_discount: invalid kind "${input.kind}" — must be percentage/fixed/free-shipping`,
    };
  }

  const { readDiscounts, writeDiscounts } = await import("./shop-store");
  const existing = await readDiscounts(slug);
  const idx = existing.findIndex((d) => d.code === code);
  const now = new Date().toISOString();

  const rawAmount =
    typeof input.amount === "number" ? Math.max(0, input.amount) : 0;
  // free-shipping isn't a first-class type in the schema yet — store as fixed 0 + tagged via code naming convention. Caller-facing summary still reads naturally.
  const schemaType: "percentage" | "fixed" =
    input.kind === "percentage" ? "percentage" : "fixed";
  const value =
    input.kind === "percentage" ? Math.min(100, rawAmount) : rawAmount;

  const newRow = {
    id:
      idx >= 0 && existing[idx]
        ? existing[idx]!.id
        : `d_${Math.random().toString(36).slice(2, 8)}`,
    code,
    type: schemaType,
    value,
    minOrderCents:
      typeof input.minOrderCents === "number"
        ? Math.max(0, Math.round(input.minOrderCents))
        : 0,
    maxUses:
      typeof input.maxUses === "number" && input.maxUses > 0
        ? Math.round(input.maxUses)
        : null,
    usesCount: idx >= 0 && existing[idx] ? existing[idx]!.usesCount : 0,
    startsAt: idx >= 0 && existing[idx] ? existing[idx]!.startsAt : now,
    endsAt: idx >= 0 && existing[idx] ? existing[idx]!.endsAt : null,
    status: (input.active === false ? "paused" : "active") as
      | "active"
      | "paused"
      | "expired",
    createdAt: idx >= 0 && existing[idx] ? existing[idx]!.createdAt : now,
    updatedAt: now,
  };

  const next = [...existing];
  if (idx >= 0) next[idx] = newRow;
  else next.push(newRow);
  await writeDiscounts(slug, next);

  const display =
    input.kind === "percentage"
      ? `${rawAmount}% off`
      : input.kind === "fixed"
        ? `$${(rawAmount / 100).toFixed(2)} off`
        : "free shipping";
  return {
    ok: true,
    summary: `Discount ${code} (${display}) ${idx >= 0 ? "updated" : "created"}${newRow.minOrderCents > 0 ? `, min order $${(newRow.minOrderCents / 100).toFixed(2)}` : ""}${newRow.maxUses ? `, cap ${newRow.maxUses} uses` : ""}.`,
  };
}

const DEFERRED: Record<string, string> = {};

export async function executeTool(
  name: string,
  input: Record<string, unknown>,
): Promise<ToolExecutionResult> {
  const handler = HANDLERS[name];
  if (handler) return handler(input);
  const deferredAt = DEFERRED[name];
  if (deferredAt) {
    return {
      ok: false,
      summary: `Tool "${name}" is recognized but not yet wired (${deferredAt}).`,
      note: `Per OPERATOR_DIRECTIVE_2026-05-09 the tool exists in the registry but its handler ships in a follow-up loop tick. The model should tell the operator that capability is "coming online soon".`,
    };
  }
  return {
    ok: false,
    summary: `Unknown tool "${name}".`,
    note: "Model invoked a tool not in BUSINESS_TOOLS. Reject and ask for clarification.",
  };
}
