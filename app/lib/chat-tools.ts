// Resolve the origin to call our own routes from a serverless function.
// Order: NEXT_PUBLIC_SITE_URL (explicit) → https://VERCEL_URL (auto-set by
// Vercel on every deploy, includes Preview deployments) → localhost.
// Without this, the chat-tool fetch from shell-zone falls back to
// http://localhost:3000 in production and ECONNREFUSEDs as "fetch failed".
function resolveOrigin(): string {
  const explicit = process.env["NEXT_PUBLIC_SITE_URL"];
  if (explicit && explicit.length > 0) return explicit.replace(/\/+$/, "");
  const vercel = process.env["VERCEL_URL"];
  if (vercel && vercel.length > 0) return `https://${vercel}`;
  return "http://localhost:3000";
}

